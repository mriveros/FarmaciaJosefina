    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        String Nombre = txtNombre.getText().trim();
        String Ruta = txtRuta.getText().trim();
        
        if("".equals(Nombre)){
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del archivo que desea guardar","Ingrese",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if("".equals(Ruta)){
            JOptionPane.showMessageDialog(this, "Seleccione el destino del archivo","Seleccione",JOptionPane.ERROR_MESSAGE);
            return;
        }
       
            Respaldo.CrearRespaldo(Ruta, Nombre);
            this.dispose();
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

