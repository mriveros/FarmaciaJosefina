    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Recibido = txtRecibido.getText().trim();
        
        if("".equals(Recibido)){
           JOptionPane.showMessageDialog(this, "Ingrese la cantidad de dinero recibido","Ingrese",JOptionPane.ERROR_MESSAGE);  
       return;
        }
  
        double Dinero = Double.parseDouble(Recibido);
     
    if( "D칩lares".equals(Moneda)){
        Dinero = Dinero*Tipo;
    }
        
   if(Dinero>=Pago){
       Fc.setRecibido(Dinero);
       dispose();
   } 
   else{
       
       if("C칩rdobas".equals(Moneda)){
       JOptionPane.showMessageDialog(this, "Cantidad insuficiente para cancelar, su monto es de C$ "+Pago);
       txtRecibido.setText("");
      
       }
       else{
           JOptionPane.showMessageDialog(this, "Cantidad insuficiente para cancelar, su monto es de $ "+Pago);
       txtRecibido.setText("");
     
       }
     
       
   }
        
   
    }//GEN-LAST:event_jButton1ActionPerformed

