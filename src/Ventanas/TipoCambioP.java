    private void txtNewCambioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNewCambioKeyTyped
char a = evt.getKeyChar();


if(!Character.isDigit(a)&&!Character.isISOControl(a)&&a!='.'){
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
            return;
}


        if("0".equals(txtNewCambio.getText())&&txtNewCambio.getCaretPosition()==1&&a!='.'&&!Character.isISOControl(a)){
        txtNewCambio.setText(txtNewCambio.getText()+".");
//        return;
        }

        if (a == '.' && txtNewCambio.getText().contains(".")) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
            return;
        }

        String x1 = "";
         if(Character.isDigit(a)||(Character.isISOControl(a)&&!"".equals(txtNewCambio.getText()))){
            x1 = txtNewCambio.getText();
        }
        if(Character.isDigit(a)||(a == '.')){
            x1 = txtNewCambio.getText().concat(String.valueOf(a));
        }
        
 
         if(!"".equals(x1)){
         
     try{ 

        Double x = Double.parseDouble(x1);  
        
         if(x>Double.MAX_VALUE){
          Toolkit.getDefaultToolkit().beep();
          evt.consume();    
         }
        
     }catch(NumberFormatException ex){
            Toolkit.getDefaultToolkit().beep();
            evt.consume();   
             }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtNewCambioKeyTyped
