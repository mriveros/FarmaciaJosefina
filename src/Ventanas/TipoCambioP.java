    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       
        MP.setVisible(false);
         GuardarLeerAutoBCN BCN = new GuardarLeerAutoBCN();
         boolean flag = false;
        try {
            flag =BCN.DatoBCN();
        } catch (IOException ex) {
         }
        
        if(flag){
            jToggleButton1.setSelected(true);
        }
        
        double cambio=0;
        int ID = 0;
        
      try{  
        resultado = Conexion.consulta("Select  Max(ID_Tipo_Cambio) from [dbo].[Tipo_Cambio]");
       while (resultado.next()){
          ID = resultado.getInt(1);
       }
      
       
       resultado = Conexion.consulta("Select  [Tipo_Cambio] from [dbo].[Tipo_Cambio] where [ID_Tipo_Cambio] = "+ID);
       while (resultado.next()){
           cambio = resultado.getDouble(1);
       }
       
      }catch(SQLException ex){
          
      }
       
        txtactual.setText(String.valueOf(cambio));
    }//GEN-LAST:event_formWindowOpened

